colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']

def display_rainbow(colors)
  print "#{colors.first[0]}: #{colors[0]}, "
  print "#{colors.first[1]}: #{colors[1]}, "
  print "#{colors.first[2]}: #{colors[2]}, "
  print "#{colors.first[3]}: #{colors[3]}, "
  print "#{colors.first[4]}: #{colors[4]}, "
  print "#{colors.first[5]}: #{colors[5]}, "
  print "#{colors.first[6]}: #{colors[6]}, "
end
